// 9. Выведитевсечисла,кратные5или3,измассива[2, 5,9,15, 0, 4].
// Методы: Используйтециклwhile.
//  Входные: [2, 5, 9, 15, 0, 4] → Результат: [5, 9 15, 0]

const arr = [2, 5, 9, 15, 0, 4];
const res = [];

let i=0;
while (i<arr.length) {
    if(arr[i]%3===0 || arr[i]%5===0){
        res.push(arr[i]);
    }
    i++;
}
console.log(res);
